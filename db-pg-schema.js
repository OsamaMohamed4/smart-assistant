// Postgres schema DDL — single source of truth shared by the migration
// script (scripts/migrate-to-pg.js) and the postgres driver boot
// (db-postgres.js). TEXT timestamps keep SQLite datetime('now') string
// semantics — see docs/postgres-migration-plan.md for rationale.
const NOW_TEXT = `to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')`;

const DDL = `
CREATE EXTENSION IF NOT EXISTS vector;

CREATE TABLE IF NOT EXISTS schema_migrations (
  id          BIGINT PRIMARY KEY,
  name        TEXT NOT NULL UNIQUE,
  applied_at  TEXT NOT NULL DEFAULT ${NOW_TEXT}
);

CREATE TABLE IF NOT EXISTS users (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email           TEXT NOT NULL,
  password_hash   TEXT NOT NULL,
  name            TEXT,
  role            TEXT NOT NULL DEFAULT 'owner',
  failed_logins   BIGINT DEFAULT 0,
  locked_until    TEXT,
  last_login_at   TEXT,
  created_at      TEXT DEFAULT ${NOW_TEXT},
  company_id      TEXT
);
CREATE UNIQUE INDEX IF NOT EXISTS users_email_lower ON users (lower(email));

CREATE TABLE IF NOT EXISTS companies (
  id              TEXT PRIMARY KEY,
  user_id         BIGINT,
  name            TEXT NOT NULL,
  language        TEXT DEFAULT 'ar-SA',
  voice_id        TEXT,
  phone_number    TEXT,
  assistant_id    TEXT,
  assistant_id_inbound TEXT,
  system_prompt   TEXT NOT NULL,
  kb_text         TEXT,
  settings        TEXT,
  last_synced_at  TEXT,
  created_at      TEXT DEFAULT ${NOW_TEXT},
  updated_at      TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_companies_user ON companies(user_id);

CREATE TABLE IF NOT EXISTS sessions (
  id           TEXT PRIMARY KEY,
  user_id      BIGINT NOT NULL,
  created_at   TEXT DEFAULT ${NOW_TEXT},
  expires_at   TEXT NOT NULL,
  last_seen_at TEXT,
  user_agent   TEXT,
  ip           TEXT
);
CREATE INDEX IF NOT EXISTS idx_sessions_user ON sessions(user_id);

CREATE TABLE IF NOT EXISTS auth_events (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id    BIGINT,
  email      TEXT,
  event_type TEXT NOT NULL,
  ip         TEXT,
  user_agent TEXT,
  created_at TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_auth_events_user ON auth_events(user_id);

CREATE TABLE IF NOT EXISTS chats (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id    TEXT NOT NULL,
  session_id    TEXT NOT NULL,
  user_message  TEXT NOT NULL,
  assistant_reply TEXT,
  channel       TEXT DEFAULT 'text',
  latency_ms    BIGINT,
  summary       TEXT,
  user_id       BIGINT,
  created_at    TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_chats_company ON chats(company_id);
CREATE INDEX IF NOT EXISTS idx_chats_session ON chats(session_id);
CREATE INDEX IF NOT EXISTS idx_chats_created ON chats(created_at);
CREATE INDEX IF NOT EXISTS idx_chats_company_created ON chats(company_id, created_at);

CREATE TABLE IF NOT EXISTS calls (
  id              TEXT PRIMARY KEY,
  company_id      TEXT,
  assistant_id    TEXT,
  caller_number   TEXT,
  duration_sec    BIGINT,
  started_at      TEXT,
  ended_at        TEXT,
  ended_reason    TEXT,
  transcript      TEXT,
  summary         TEXT,
  cost_usd        DOUBLE PRECISION,
  direction       TEXT NOT NULL DEFAULT 'inbound',
  recording_url   TEXT,
  structured_data TEXT,
  created_at      TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_calls_company ON calls(company_id);
CREATE INDEX IF NOT EXISTS idx_calls_direction ON calls(direction);
CREATE INDEX IF NOT EXISTS idx_calls_created ON calls(created_at);
CREATE INDEX IF NOT EXISTS idx_calls_company_created ON calls(company_id, created_at);

CREATE TABLE IF NOT EXISTS kb_documents (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id   TEXT NOT NULL,
  filename     TEXT NOT NULL,
  mime_type    TEXT,
  size_bytes   BIGINT,
  raw_text     TEXT,
  raw_data     BYTEA,
  deleted_at   TEXT,
  created_at   TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_kbdocs_company ON kb_documents(company_id);

CREATE TABLE IF NOT EXISTS kb_chunks (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id    TEXT NOT NULL,
  document_id   BIGINT NOT NULL,
  chunk_index   BIGINT NOT NULL,
  text          TEXT NOT NULL,
  embedding     vector(1536) NOT NULL,
  token_count   BIGINT,
  created_at    TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_kbchunks_company ON kb_chunks(company_id);
CREATE INDEX IF NOT EXISTS idx_kbchunks_doc     ON kb_chunks(document_id);

CREATE TABLE IF NOT EXISTS audit_events (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  actor_id     BIGINT,
  actor_email  TEXT,
  action       TEXT NOT NULL,
  resource     TEXT,
  metadata     TEXT,
  ip           TEXT,
  user_agent   TEXT,
  created_at   TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_audit_actor  ON audit_events(actor_id);
CREATE INDEX IF NOT EXISTS idx_audit_action ON audit_events(action);

CREATE TABLE IF NOT EXISTS usage_counters (
  company_id   TEXT NOT NULL,
  day          TEXT NOT NULL,
  kind         TEXT NOT NULL,
  amount       BIGINT NOT NULL DEFAULT 0,
  PRIMARY KEY (company_id, day, kind)
);

CREATE TABLE IF NOT EXISTS webhook_events (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  provider      TEXT NOT NULL,
  event_id      TEXT,
  event_type    TEXT,
  raw_body      TEXT NOT NULL,
  status        TEXT NOT NULL DEFAULT 'pending',
  attempts      BIGINT NOT NULL DEFAULT 0,
  last_error    TEXT,
  received_at   TEXT NOT NULL DEFAULT ${NOW_TEXT},
  processed_at  TEXT,
  UNIQUE (provider, event_id)
);
CREATE INDEX IF NOT EXISTS idx_webhook_events_status ON webhook_events(status);

CREATE TABLE IF NOT EXISTS scenarios (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id          TEXT NOT NULL,
  name                TEXT NOT NULL,
  description         TEXT,
  first_message       TEXT,
  first_message_inbound TEXT,
  instruction_prompt  TEXT NOT NULL,
  instruction_prompt_inbound TEXT,
  success_criteria    TEXT,
  variables           TEXT,
  is_active           BIGINT NOT NULL DEFAULT 1,
  language            TEXT DEFAULT 'ar',
  knowledge_base_ids  TEXT,
  created_at          TEXT DEFAULT ${NOW_TEXT},
  updated_at          TEXT DEFAULT ${NOW_TEXT},
  deleted_at          TEXT
);
CREATE INDEX IF NOT EXISTS idx_scenarios_company ON scenarios(company_id);
CREATE INDEX IF NOT EXISTS idx_scenarios_active  ON scenarios(company_id, is_active, deleted_at);

CREATE TABLE IF NOT EXISTS scenario_versions (
  id                     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  scenario_id            BIGINT NOT NULL,
  name                   TEXT,
  first_message          TEXT,
  first_message_inbound  TEXT,
  instruction_prompt     TEXT,
  edited_by              TEXT,
  created_at             TEXT NOT NULL DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_scenario_versions_sc ON scenario_versions(scenario_id, created_at DESC);

CREATE TABLE IF NOT EXISTS whatsapp_sessions (
  company_id     TEXT NOT NULL,
  customer_phone TEXT NOT NULL,
  vapi_chat_id   TEXT,
  updated_at     TEXT NOT NULL DEFAULT ${NOW_TEXT},
  PRIMARY KEY (company_id, customer_phone)
);

CREATE TABLE IF NOT EXISTS api_keys (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id   TEXT NOT NULL,
  name         TEXT,
  key_hash     TEXT NOT NULL UNIQUE,
  prefix       TEXT NOT NULL,
  created_at   TEXT NOT NULL DEFAULT ${NOW_TEXT},
  last_used_at TEXT,
  revoked_at   TEXT
);
CREATE INDEX IF NOT EXISTS idx_api_keys_company ON api_keys(company_id);

CREATE TABLE IF NOT EXISTS campaigns (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id      TEXT NOT NULL,
  name            TEXT NOT NULL,
  status          TEXT NOT NULL DEFAULT 'draft',
  start_hour      BIGINT NOT NULL DEFAULT 10,
  end_hour        BIGINT NOT NULL DEFAULT 21,
  max_concurrent  BIGINT NOT NULL DEFAULT 2,
  max_attempts    BIGINT NOT NULL DEFAULT 2,
  retry_delay_min BIGINT NOT NULL DEFAULT 60,
  created_at      TEXT DEFAULT ${NOW_TEXT},
  updated_at      TEXT DEFAULT ${NOW_TEXT},
  started_at      TEXT,
  completed_at    TEXT
);
CREATE INDEX IF NOT EXISTS idx_campaigns_company ON campaigns(company_id);
CREATE INDEX IF NOT EXISTS idx_campaigns_status  ON campaigns(status);

CREATE TABLE IF NOT EXISTS campaign_contacts (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  campaign_id     BIGINT NOT NULL,
  phone           TEXT NOT NULL,
  name            TEXT,
  variables       TEXT,
  status          TEXT NOT NULL DEFAULT 'pending',
  attempts        BIGINT NOT NULL DEFAULT 0,
  last_attempt_at TEXT,
  call_id         TEXT,
  last_error      TEXT,
  created_at      TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_cc_campaign_status ON campaign_contacts(campaign_id, status);
CREATE INDEX IF NOT EXISTS idx_cc_call ON campaign_contacts(call_id);

CREATE TABLE IF NOT EXISTS eval_questions (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id  TEXT NOT NULL,
  question    TEXT NOT NULL,
  expected    TEXT NOT NULL,
  created_at  TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_evalq_company ON eval_questions(company_id);

CREATE TABLE IF NOT EXISTS eval_runs (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id    TEXT NOT NULL,
  label         TEXT,
  score         DOUBLE PRECISION,
  total         BIGINT,
  correct       BIGINT,
  partial       BIGINT,
  results       TEXT,
  created_at    TEXT DEFAULT ${NOW_TEXT}
);
CREATE INDEX IF NOT EXISTS idx_evalr_company ON eval_runs(company_id);
`;

module.exports = { DDL, NOW_TEXT };
